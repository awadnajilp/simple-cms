function execute_scalar($sql,$def="") {
    $rs = mysql_query($sql) or die("bad query");
    if (mysql_num_rows($rs)) {
        $r = mysql_fetch_row($rs);
        mysql_free_result($rs);
        return $r[0];
        mysql_free_result($rs);
    }
    return $def;
}
 
function execute_row($sql) {
    $rs = mysql_query($sql) or die("bad query");
    if (mysql_num_rows($rs)) {
        $r = mysql_fetch_array($rs);
        mysql_free_result($rs);
        return $r;
    }
    mysql_free_result($rs);
    return "";
}
